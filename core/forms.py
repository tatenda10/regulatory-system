class IFRS17FileUploadForm(forms.ModelForm):
    """Form for uploading IFRS 17 data files."""
    
    class Meta:
        model = IFRS17Submission
        fields = ['institution', 'reporting_period', 'uploaded_file', 'file_type', 'notes']
        widgets = {
            'institution': forms.Select(attrs={
                'class': 'w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500'
            }),
            'reporting_period': forms.DateInput(attrs={
                'class': 'w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500',
                'type': 'date'
            }),
            'file_type': forms.Select(attrs={
                'class': 'w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500'
            }),
            'uploaded_file': forms.FileInput(attrs={
                'class': 'w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500',
                'accept': '.csv,.xlsx,.xbrl'
            }),
            'notes': forms.Textarea(attrs={
                'class': 'w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500',
                'rows': 3
            }),
        }
    
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        # Only show active institutions
        self.fields['institution'].queryset = Institution.objects.filter(status='active')
        
        # Set required fields explicitly
        self.fields['institution'].required = True
        self.fields['reporting_period'].required = True
        self.fields['uploaded_file'].required = True
        self.fields['file_type'].required = True
        
        # Make required fields more obvious
        for field_name, field in self.fields.items():
            if field.required:
                field.widget.attrs['required'] = True
